#include <bits/stdc++.h>
using namespace std;
int tc, a, b;
int main(){
    cin >> tc;
    while(tc--){
        cin >> a >> b;
        if(a == 1) cout << a;
        else if(a % 10 == 0) cout << 10;
        else {
            b = b % 4 == 0 ? 4 : b % 4;
            int sum = 1;
            while(b--) sum *= a;
            cout << sum % 10;
        }
        cout << '\n';
    }
}

