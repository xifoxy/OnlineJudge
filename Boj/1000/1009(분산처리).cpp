// a제곱b의 형태로 주어지는데 1 <= a < 1e2 , 1 <= b < 1e6 
// 언뜻보면 엄청난 숫자의 범위에 압도당하기 쉽다.
// 하지만 곱셈의 규칙을 찾아낸다면 어렵지 않다.
// 2 -> 4 -> 8 -> 16 -> 3(2)
// 3 -> 9 -> 27 -> 81 -> 24(3)
// 4 -> 16 -> 6(4)
// 5 -> 25 -> 12(5)
// 6 -> 3(6)
