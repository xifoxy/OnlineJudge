//N부터 구해내려 가는 방식과 1부터 구해 올라가는 방식이 있는데
//나는 올라가는 방식으로 풀었다.
//BaseCase는 2와 3이 1이 된다. 이해가 안된다면 생각해보자.
//그리고 N까지 올라가면서 순차적으로 쌓아올라가면 된다.
//만약 i가 2 혹은 3으로 나누어 떨어지면
//min(d[i], d[i/X] + 1)로 값을 구해주면된다.